# Arithmetic Operators
a = 10
b = 3

addition = a + b          # Addition: 13
subtraction = a - b       # Subtraction: 7
multiplication = a * b    # Multiplication: 30
division = a / b          # Division: 3.333...
modulus = a % b           # Modulus: 1
exponentiation = a ** b   # Exponentiation: 1000
floor_division = a // b   # Floor Division: 3

# Comparison Operators
equal = (a == b)          # Equal: False
not_equal = (a != b)      # Not equal: True
greater_than = (a > b)    # Greater than: True
less_than = (a < b)       # Less than: False
greater_equal = (a >= b)  # Greater than or equal to: True
less_equal = (a <= b)     # Less than or equal to: False

# Logical Operators
x = True
y = False

logical_and = x and y     # Logical AND: False
logical_or = x or y       # Logical OR: True
logical_not = not x       # Logical NOT: False

# Assignment Operators
c = 5
c += 2                    # Increment assignment: c = 7
c -= 2                    # Decrement assignment: c = 5
c *= 2                    # Multiplication assignment: c = 10
c /= 2                    # Division assignment: c = 5.0
c %= 2                    # Modulus assignment: c = 1.0
c **= 2                   # Exponentiation assignment: c = 1.0
c //= 2                   # Floor division assignment: c = 0.0

# Identity Operators
d = 10
identity_is = (d is a)    # Identity 'is': True
identity_is_not = (d is not b)  # Identity 'is not': True

# Membership Operators
numbers = [1, 2, 3, 4, 5]
membership_in = (a in numbers)    # Membership 'in': False
membership_not_in = (b not in numbers)  # Membership 'not in': True

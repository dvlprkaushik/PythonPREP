# Integer to Float
a = 5
a_float = float(a)        # 5 -> 5.0

# Float to Integer
b = 7.8
b_int = int(b)            # 7.8 -> 7

# Integer to String
c = 10
c_str = str(c)            # 10 -> '10'

# String to Integer
d = "15"
d_int = int(d)            # '15' -> 15

# String to Float
e = "3.14"
e_float = float(e)        # '3.14' -> 3.14

# Integer to Boolean
f = 0
f_bool = bool(f)          # 0 -> False (Any non-zero value would become True)

# Float to String
g = 8.9
g_str = str(g)            # 8.9 -> '8.9'

# Boolean to Integer
h = True
h_int = int(h)            # True -> 1 (False would become 0)

# None to String
i = None
i_str = str(i)            # None -> 'None'
